// Widen the literal types (from `as const`) so `en` and `ar` share one shape.
type Widen<T> = {
  [K in keyof T]: T[K] extends object ? { [P in keyof T[K]]: string } : string;
};
export type Dictionary = Widen<(typeof dictionary)["en"]>;

export const getDictionary = (lang: Lang): Dictionary =>
  dictionary[lang] as unknown as Dictionary;
